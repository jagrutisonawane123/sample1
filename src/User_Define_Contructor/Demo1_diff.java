package User_Define_Contructor;

public class Demo1_diff 
{
	int i;
	int j;
	
	
	Demo1_diff()
	{
		i=55;
		j=44;
		
	}
	public void add()
	{
		System.out.println(i+j);
	}
	public void mul()
	{
		System.out.println(i*j);
	}
	public void sub()
	{
		System.out.println(i-j);
	}
	

}
