package User_Define_Contructor;

public class Demo5_diff
{

	int s;
	int r;
	
	
	Demo5_diff()
	{
		s=10;
		r=2;
		
	}
	public void add() 
	{
		System.out.println(s+r);
		
	}
	public void  mul() 
	{
		
		System.out.println(s*r);
		
	}
	public void sub() 
	{
		System.out.println(s-r);
		
	}
	public void div() 
	{
		System.out.println(s/r);
		
	}
}
