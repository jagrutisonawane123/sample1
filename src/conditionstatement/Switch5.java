package conditionstatement;

public class Switch5 
{
	public static void main(String[] args)
	{
		int num=9;
		switch(num)
		{
		case 1:System.out.println("print decimal number 1");
		break;
		case 2:System.out.println("print decimal number 2");
		break;
		case 3:System.out.println("print decimal number 3");
		break;
		case 4:System.out.println("print decimal number 4");
		break;
		case 5:System.out.println("print decimal number 5");
		break;
		default:System.out.println("number is not found");
		break;
		
		
		
		}
		
	}

}
