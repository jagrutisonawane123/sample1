package conditionstatement;

public class Switch2 
{
	public static void main(String[] args)
	{
		String name="JJ";
		
		switch(name)
		{
		case "BI":System.out.println("Running BI code");
		break;

		case "CD":System.out.println("Running CD code");
		break;
		
		case "CW":System.out.println("Running CW code");
		break;
		
		case "BC":System.out.println("Running BC code");
		break;
		
		case "SM":System.out.println("Running SM code");
		break;
		
		default:System.out.println("invalid statement");
		break;
			
		}
		
	}

}
