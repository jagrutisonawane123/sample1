package conditionstatement;

public class Switchh11 
{
	public static void main(String[] args)
	{
		
		int n=3;
		switch(n)
		{
		case 1:System.out.println("number is print 2");
		break;
		case 2:System.out.println("number is print 4");
		break;
		case 3:System.out.println("number is print 6");
		break;
		case 4:System.out.println("number is print 8");
		break;
		case 5:System.out.println("number is print 10");
		break;
		default:System.out.println("invalid input");
		break;
		}
		
	}

}
